#include "server.hpp"
#include <iostream>

// ----------------------------------------------------------------------------
// 构造函数
// ----------------------------------------------------------------------------
Server::Server(asio::io_context& io, short port, AgentLoop& agent)
    : acceptor_(io, tcp::endpoint(tcp::v4(), port))  // 绑定地址和端口
    , agent_(agent) {
    
    std::cout << "[i] Server binding to port " << port << "...\n";
    
    // 开始接受连接
    do_accept();
}

// ----------------------------------------------------------------------------
// 启动异步接受
// ----------------------------------------------------------------------------
void Server::do_accept() {
    // async_accept 参数说明：
    // 1. socket: 新连接的 socket（由 Asio 填充）
    // 2. callback: 接受完成后的回调
    acceptor_.async_accept(
        // lambda 捕获 this，回调中使用成员变量
        [this](boost::system::error_code ec, tcp::socket socket) {
            this->on_accept(ec, std::move(socket));
        }
    );
}

// ----------------------------------------------------------------------------
// 接受完成回调
// ----------------------------------------------------------------------------
void Server::on_accept(boost::system::error_code ec, tcp::socket socket) {
    if (!ec) {
        // ---------------------------------------------------------------------
        // 创建 Session 处理新连接
        // ---------------------------------------------------------------------
        // shared_ptr 的原因：
        // - Session 的生命周期由异步操作决定
        // - 可能在任意时刻有新消息到达
        // - 需要自动管理内存，避免泄漏
        // ---------------------------------------------------------------------
        std::make_shared<WsSession>(
            std::move(socket),  // 将 socket 移入 Session
            agent_              // 传递 Agent 引用
        )->start();             // 启动 Session
    } else {
        std::cerr << "[✗] Accept error: " << ec.message() << "\n";
    }
    
    // ---------------------------------------------------------------------
    // 继续接受下一个连接（关键！不调用就不会接受新连接）
    // ---------------------------------------------------------------------
    do_accept();
}
